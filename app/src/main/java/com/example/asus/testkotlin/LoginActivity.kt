    /**
     * Attempts to sign in or register the account specified by the login form.
     * If there are form errors (invalid email, missing fields, etc.), the
     * errors are presented and no actual login attempt is made.
     */
    private fun attemptLogin() {
        if (mAuthTask != null) {
            return
        }

        // Reset errors.
        email.error = null
        password.error = null

        // Store values at the time of the login attempt.
        val emailStr = email.text.toString()
        val passwordStr = password.text.toString()
        usr = emailStr
        pswd = passwordStr

        var cancel = false
        var focusView: View? = null

        // Check for a valid password, if the user entered one.
        if (!TextUtils.isEmpty(passwordStr) && !isPasswordValid(passwordStr)) {
            password.error = getString(R.string.error_invalid_password)
            focusView = password
            cancel = true
        }

        // Check for a valid email address.
        if (TextUtils.isEmpty(emailStr)) {
            email.error = getString(R.string.error_field_required)
            focusView = email
            cancel = true
        } else if (!isEmailValid(emailStr)) {
            email.error = getString(R.string.error_invalid_email)
            focusView = email
            cancel = true
        }

        if (cancel) {
            // There was an error; don't attempt login and focus the first
            // form field with an error.
            focusView?.requestFocus()
        } else {
            // Show a progress spinner, and kick off a background task to
            // perform the user login attempt.

            //mAuthTask = UserLoginTask(emailStr, passwordStr)
            //mAuthTask!!.execute(null as Void?)
            //var hasil : String

            HttpAsyncTaskPost().execute("http://10.0.2.2:8070/maps/login.php?username=$usr&password=$pswd")
           // Toast.makeText(getApplicationContext(),""+hasil,Toast.LENGTH_SHORT).show()
            if (hasil.equals("login success"))
            {
                Toast.makeText(getApplicationContext(),"Login Sukses",Toast.LENGTH_SHORT).show()
                showProgress(true)
                val intent = Intent(applicationContext, MainHome::class.java)
                startActivity(intent)
            }
            else
            {
                showProgress(false)
                Toast.makeText(getApplicationContext(),"Login Gagal",Toast.LENGTH_SHORT).show()
            }

        }
    }

